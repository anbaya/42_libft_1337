#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*r;

	r = (char *)s;
	while (*s)
		s++;
	while (r != s && (unsigned char)*s != c)
		s--;
	if ((unsigned char)*s == c)
		return ((char *)s);
	return (NULL);
}
